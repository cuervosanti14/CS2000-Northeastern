# User input (degrees Fahrenheit)
fahrenheit = float(input("Enter degrees Fahrenheit: "))

# Calculation (degrees Celsius)
celsius = (5/9) * (fahrenheit - 32)

# Output (degrees Celsius)
print("Degrees Celsius = ", celsius)